list_1 = ["Ram", 23, 6.2]

# Length of the list
len(list_1)
# => 3

list_2 = ["Sita", 20, 5.2]

# List Concatenation
list_1 + list_2
# => ['Ram', 23, 6.2, 'Sita', 20, 5.2]

# Repetition
print(["Hello"]*3)
# => ['Hello', 'Hello', 'Hello']

# Membership
3 in [1, 2, 3]

# Find Maximum
max([1, 2, 3])
# => 3

# Find Minimum
min([1, 2, 3])
# => 1
